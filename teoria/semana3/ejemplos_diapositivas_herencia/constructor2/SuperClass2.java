public class SuperClass2 {  
  public SuperClass2() {    
    System.out.println("Este es el constructor" +
                       " de la superclase" + 
                       " sin argumentos.");
  }

  public SuperClass2(int arg) {    
    System.out.println("El siguiente argumento" +
                       " fue pasado al constructor" + 
                       " de la superclase: " + arg);
  }
}

