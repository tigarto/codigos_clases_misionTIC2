public class EjemploFor1 {
    public static void main(String[] args) {
        System.out.println("Ciclo for 1");
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        System.out.println("Ciclo for 2");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        System.out.println("Ciclo for 3");
        for (int i = 200; i < 211; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        System.out.println("Ciclo for 4");
        for (int i = 200; i >= 190; i--) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        System.out.println("Ciclo for 5");
        for (double i = 1.0; i <= 20; i += 0.5) {
            System.out.print(i + " ");
        }
        System.out.println();
        
        
    }
    
}
